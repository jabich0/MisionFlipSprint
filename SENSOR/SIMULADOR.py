class GreenDeliveryPharmaIoTSensor:
    """
    Simulador de sensor IoT para GreenDelivery - Divisi√≥n Farmac√©utica
    Monitorea temperatura, humedad, GPS y aceler√≥metro para transporte de medicamentos
    """
    
    def __init__(self, envio_id=None, endpoint_url=None, ubicacion_inicio=None):
        """
        Inicializa el sensor IoT farmac√©utico
        
        Args:
            envio_id (str): ID √∫nico del env√≠o farmac√©utico (ej: "GD-MEDS-001")
            endpoint_url (str): URL del endpoint en la nube
            ubicacion_inicio (tuple): (latitud, longitud) de inicio
        """
        
        self.envio_id = envio_id or f"GD-MEDS-{random.randint(1000, 9999)}"
        self.endpoint_url = endpoint_url or "hhttps://greendelivery-pharma-gateway-587952111457.europe-west1.run.app"
        
        # Ubicaci√≥n inicial (por defecto Madrid, Espa√±a - Centro de Distribuci√≥n Farmac√©utica)
        if ubicacion_inicio:
            self.latitud, self.longitud = ubicacion_inicio
        else:
            self.latitud = 40.4168 + random.uniform(-0.02, 0.02)  # Madrid con variaci√≥n
            self.longitud = -3.7038 + random.uniform(-0.02, 0.02)
        
        # Par√°metros base del env√≠o farmac√©utico
        self.temperatura_base = random.uniform(3.0, 6.0)  # Temperatura refrigeraci√≥n farmac√©utica (2-8¬∞C)
        self.humedad_base = random.uniform(70, 85)        # Humedad t√≠pica para medicamentos
        self.velocidad_movimiento = random.uniform(0.0003, 0.0008)  # Velocidad GPS para medicamentos
        
    def generar_datos_sensor(self):
        """
        Genera datos realistas del sensor IoT farmac√©utico
        
        Returns:
            dict: Datos del sensor con validaciones farmac√©uticas
        """
        
        # Simular variaciones realistas en cadena de fr√≠o
        variacion_temp = random.uniform(-1.5, 2.5)
        if random.random() < 0.15:  # 15% probabilidad de alerta t√©rmica
            variacion_temp += random.uniform(3.0, 8.0)  # Ruptura cadena de fr√≠o
            
        temperatura = max(0, self.temperatura_base + variacion_temp)
        
        # Humedad cr√≠tica para medicamentos
        humedad = max(30, min(95, self.humedad_base + random.uniform(-10, 15)))
        
        # Simulaci√≥n de movimiento (rutas de distribuci√≥n farmac√©utica)
        self.latitud += random.uniform(-self.velocidad_movimiento, self.velocidad_movimiento)
        self.longitud += random.uniform(-self.velocidad_movimiento, self.velocidad_movimiento)
        
        # Aceler√≥metro (detecci√≥n de impactos cr√≠ticos para viales/ampollas)
        aceleracion_base = random.uniform(0.8, 1.5)
        if random.random() < 0.08:  # 8% probabilidad de impacto fuerte
            aceleracion_base += random.uniform(2.0, 6.0)  # Impacto que podr√≠a da√±ar medicamentos
            
        sensor_data = {
            "ID_envio": self.envio_id,
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "temperatura": round(temperatura, 2),
            "humedad": round(humedad, 1),
            "latitud": round(self.latitud, 6),
            "longitud": round(self.longitud, 6),
            "acelerometro_ejeZ": round(aceleracion_base, 2),
            "estado_sensor": "ACTIVO",
            "bateria": round(random.uniform(85, 100), 1),
            "tipo_carga": "MEDICAMENTOS",
            "temperatura_objetivo": "2-8¬∞C"
        }
        
        return sensor_data
    
    def detectar_alertas_farmaceuticas(self, datos):
        """
        Detecta alertas cr√≠ticas para medicamentos
        
        Args:
            datos (dict): Datos del sensor
            
        Returns:
            list: Lista de alertas detectadas
        """
        alertas = []
        
        # Alerta cr√≠tica: Cadena de fr√≠o rota
        if datos["temperatura"] > 8.0:
            alertas.append({
                "tipo": "CADENA_FRIO_ROTA",
                "severidad": "CRITICA",
                "temperatura": datos["temperatura"],
                "descripcion": f"Temperatura {datos['temperatura']}¬∞C > 8¬∞C - Medicamentos en riesgo",
                "impacto": "P√©rdida potencial de eficacia medicamentos"
            })
        
        # Alerta alta: Impacto fuerte (riesgo viales/ampollas)
        if datos["acelerometro_ejeZ"] > 3.0:
            alertas.append({
                "tipo": "IMPACTO_DETECTADO",
                "severidad": "ALTA", 
                "aceleracion": datos["acelerometro_ejeZ"],
                "descripcion": f"Impacto {datos['acelerometro_ejeZ']}G > 3G - Posible da√±o a medicamentos",
                "impacto": "Riesgo de rotura de viales/ampollas"
            })
            
        # Alerta media: Humedad excesiva
        if datos["humedad"] > 90:
            alertas.append({
                "tipo": "HUMEDAD_EXCESIVA",
                "severidad": "MEDIA",
                "humedad": datos["humedad"],
                "descripcion": f"Humedad {datos['humedad']}% > 90% - Riesgo degradaci√≥n",
                "impacto": "Posible degradaci√≥n de principios activos"
            })
            
        return alertas
    
    def enviar_datos(self, datos):
        """
        Env√≠a datos al endpoint en la nube con manejo de errores
        
        Args:
            datos (dict): Datos del sensor a enviar
            
        Returns:
            bool: True si el env√≠o fue exitoso
        """
        try:
            response = requests.post(self.endpoint_url, json=datos, timeout=10)
            
            timestamp = datetime.now().strftime("%H:%M:%S")
            
            if response.status_code == 200:
                print(f"‚úÖ [{timestamp}] Datos enviados - Medicamentos: {self.envio_id}")
                
                # MOSTRAR RESPUESTA DE GOOGLE CLOUD
                try:
                    respuesta_google = response.json()
                    print(f"üöÄ RESPUESTA GOOGLE CLOUD:")
                    print(f"   üíä {respuesta_google.get('message', '')}")
                    print(f"   üì¶ Env√≠o: {respuesta_google.get('envio_id', '')}")
                    print(f"   üå°Ô∏è Temperatura: {respuesta_google.get('temperatura_actual', '')}")
                    print(f"   üìä Status: {respuesta_google.get('status', '')}")
                    print(f"   ‚úÖ Cadena fr√≠o: {respuesta_google.get('cumple_cadena_frio', '')}")
                    print(f"   ‚è∞ Procesado: {respuesta_google.get('timestamp', '')}")
                    if respuesta_google.get('alertas') and len(respuesta_google.get('alertas', [])) > 0:
                        print(f"   üö® Alertas detectadas: {len(respuesta_google.get('alertas', []))}")
                        for alerta in respuesta_google.get('alertas', []):
                            print(f"      ‚ö†Ô∏è {alerta.get('tipo', '')}: {alerta.get('impacto', '')}")
                    print(f"   üèóÔ∏è {respuesta_google.get('procesado_por', '')}")
                except Exception as e:
                    print(f"   üìù Respuesta raw: {response.text}")
                
                # Mostrar alertas locales tambi√©n
                alertas = self.detectar_alertas_farmaceuticas(datos)
                for alerta in alertas:
                    if alerta["severidad"] == "CRITICA":
                        print(f"   üö® ALERTA CRITICA: {alerta['tipo']}: {alerta['descripcion']}")
                        print(f"      üîç Impacto: {alerta['impacto']}")
                    elif alerta["severidad"] == "ALTA":
                        print(f"   ‚ö†Ô∏è ALERTA ALTA: {alerta['tipo']}: {alerta['descripcion']}")
                        print(f"      üîç Impacto: {alerta['impacto']}")
                
                # Formato t√©rmico con emojis
                temp = datos["temperatura"]
                if temp > 8.0:
                    temp_status = "üî¥"  # Fuera de rango
                elif temp < 2.0:
                    temp_status = "üîµ"  # Muy fr√≠o
                else:
                    temp_status = "üü¢"  # Correcto
                
                accel_status = "‚ö°" if datos["acelerometro_ejeZ"] > 3.0 else "üì±"
                bateria_status = "üîã" if datos["bateria"] > 20 else "ü™´"
                
                print(f"   {temp_status} {datos['temperatura']}¬∞C | üíß {datos['humedad']}% | {accel_status} {datos['acelerometro_ejeZ']}G | {bateria_status} {datos['bateria']}%")
                
                return True
                
            else:
                print(f"‚ùå [{timestamp}] Error HTTP {response.status_code}: {response.text}")
                return False
                
        except requests.exceptions.Timeout:
            print(f"‚è∞ [{datetime.now().strftime('%H:%M:%S')}] Timeout - Endpoint no responde")
            return False
        except requests.exceptions.RequestException as e:
            print(f"üì° [{datetime.now().strftime('%H:%M:%S')}] Error conexi√≥n: {str(e)}")
            return False
        except Exception as e:
            print(f"üí• [{datetime.now().strftime('%H:%M:%S')}] Error inesperado: {str(e)}")
            return False
    
    def demo_rapida(self, duracion_segundos=30):
        """
        Demostraci√≥n r√°pida del monitoreo farmac√©utico
        
        Args:
            duracion_segundos (int): Duraci√≥n de la demostraci√≥n
        """
        print(f"üé¨ MODO DEMOSTRACI√ìN FARMAC√âUTICA ACTIVADO")
        print(f"üöÄ INICIANDO MONITOREO IoT FARMAC√âUTICO")
        print(f"üíä Tipo de carga: MEDICAMENTOS (cadena de fr√≠o 2-8¬∞C)")
        print(f"‚è±Ô∏è  Intervalo: 3 segundos")
        print(f"‚è∞ Duraci√≥n: {duracion_segundos} segundos")
        print(f"üõë Presiona Ctrl+C para detener")
        print("=" * 50)
        
        inicio = time.time()
        contador = 0
        exitosos = 0
        alertas_generadas = 0
        
        try:
            while (time.time() - inicio) < duracion_segundos:
                datos = self.generar_datos_sensor()
                alertas = self.detectar_alertas_farmaceuticas(datos)
                alertas_generadas += len(alertas)
                
                if self.enviar_datos(datos):
                    exitosos += 1
                
                contador += 1
                print()  # L√≠nea en blanco para separar
                
                time.sleep(3)
                
        except KeyboardInterrupt:
            print(f"\nüõë Monitoreo detenido por usuario")
        
        duracion_real = time.time() - inicio
        print(f"\n‚è∞ Monitoreo farmac√©utico completado: {int(duracion_real)}s transcurridos")
        
        # Estad√≠sticas finales
        print("\n" + "=" * 50)
        print("üìä ESTAD√çSTICAS DEL MONITOREO FARMAC√âUTICO")
        print("=" * 50)
        print(f"üíä Env√≠o medicamentos monitoreado: {self.envio_id}")
        print(f"üì° Total de transmisiones: {contador}")
        print(f"‚úÖ Transmisiones exitosas: {exitosos} ({(exitosos/contador*100):.1f}%)" if contador > 0 else "")
        print(f"üö® Alertas farmac√©uticas generadas: {alertas_generadas}")
        print(f"üìç √öltima ubicaci√≥n: {self.latitud:.4f}, {self.longitud:.4f}")
        print(f"‚ùÑÔ∏è  Rango temperatura objetivo: 2-8¬∞C")
    
